function solution(n) {
  let answer = []
  let checkArr = Array.from({ length: n + 1 }, () => 0)
  function DFS(v) {
    if (v === n + 1) {
      let tmp = ''
      for (let i = 1; i <= n; i++) {
        if (checkArr[i] === 1) {
          tmp += i + ' '
        }
      }
      if (tmp.length > 0) {
        answer.push(tmp.trim())
      }
    } else {
      checkArr[v] = 1
      DFS(v + 1)
      checkArr[v] = 0
      DFS(v + 1)
    }
  }
  DFS(1)
  console.log(answer)
}
